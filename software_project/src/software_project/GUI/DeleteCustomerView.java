    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        if (!"".equals(FirstnameText.getText()) && !"".equals(LastNameText.getText()) && !"".equals(EmailText.getText())&& !"".equals(RegionText.getText()) && !"".equals(PasswordText.getText()) && !"".equals(IsFreezeText.getText()) && !"".equals(StreetNameText.getText())
                && !"".equals(StreetNumberText.getText()) && !"".equals(FloorNumberText.getText()) && !"".equals(MonthlyReadingText.getText()) && !"".equals(MetercodeText.getText()) && !"".equals(IsFreezeText.getText())) {
            try {
                int MetercodeSearchKey = Integer.parseInt(SearchMetercodeText.getText());
                customer.setCustomerFirstName(FirstnameText.getText());
                customer.setCustomerLastName(LastNameText.getText());
                customer.setCustomerEmail(EmailText.getText());
                customer.setCustomerRegion(RegionText.getText());
                customer.setCustomerPassword(Integer.parseInt(PasswordText.getText()));
                customer.setCustomerIsFreeze(Integer.parseInt(IsFreezeText.getText()));
                customer.setCustomerStreetName(StreetNameText.getText());
                customer.setCustomerStreetNumber(Integer.parseInt(StreetNumberText.getText()));
                customer.setCustomerFloorNumber(Integer.parseInt(FloorNumberText.getText()));
                customer.setCustomerMetercode(Integer.parseInt(MetercodeText.getText()));
                customer.deleteCustomer(MetercodeSearchKey);
                setNULL();
            } catch (NumberFormatException numberException) {
                JOptionPane.showMessageDialog(null, numberException, "Enter Integer Numbers Only", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            customer.showErrorDialog("PLEASE FILL ALL FIELDS...");

        }
    }

