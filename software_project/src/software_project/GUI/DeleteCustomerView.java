    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        if (!"".equals(SearchMetercodeText.getText())) {
            try {
                int MetercodeSearchKey = Integer.parseInt(SearchMetercodeText.getText());
                if (customer.searchCustomer(MetercodeSearchKey)) {
                    MetercodeText.setText(Integer.toString(customer.getCustomerMetercode()));
                    FirstnameText.setText(customer.getCustomerFirstName());
                    LastNameText.setText(customer.getCustomerLastName());
                    RegionText.setText(customer.getCustomerRegion());
                    StreetNameText.setText(customer.getCustomerStreetName());
                    StreetNumberText.setText(Integer.toString(customer.getCustomerStreetNumber()));
                    FloorNumberText.setText(Integer.toString(customer.getCustomerFloorNumber()));
                    MonthlyReadingText.setText(Integer.toString(customer.getCustomerMonthlyBillReading()));
                    EmailText.setText(customer.getCustomerEmail());
                    PasswordText.setText(Integer.toString(customer.getCustomerPassword()));
                    IsFreezeText.setText(Integer.toString(customer.getCustomerIsFreeze()));
                } else {
                    JOptionPane.showMessageDialog(rootPane, "This Metercode is not found", "Error", ERROR_MESSAGE);
                }
            } catch (NumberFormatException numberException) {
                JOptionPane.showMessageDialog(null, numberException, "Enter Integer Numbers Only", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            customer.showErrorDialog("Please enter the Metercode");
        }
    }//GEN-LAST:event_SearchActionPerformed

